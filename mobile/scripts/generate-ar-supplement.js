const listPath = path.join(__dirname, "missing-ar-list.txt");
const lines = fs.readFileSync(listPath, "utf8").split(/\r?\n/).slice(1);
const out = {};
let hit = 0;
let miss = 0;

for (const line of lines) {
  const sep = line.indexOf(" | ");
  if (sep < 0) continue;
  const en = line.slice(0, sep).trim();
  const fr = line.slice(sep + 3).trim();
  const ar = T[en] || T[fr];
  if (ar) {
    if (en) out[en] = ar;
    if (fr && fr !== en) out[fr] = ar;
    hit++;
  } else {
    miss++;
  }
}

const header = `/** Auto-generated — run node scripts/generate-ar-supplement.js */\nexport const arOverridesSupplement = {\n`;
const body = Object.entries(out)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
  .join("\n");
const footer = `\n};\n`;

fs.writeFileSync(path.join(__dirname, "../src/locales/arOverridesSupplement.js"), header + body + footer);
console.log(`Wrote ${Object.keys(out).length} keys (${hit} pairs, ${miss} still untranslated)`);
